package com.company;

public class Logical_Operator {
    public static void main(String[] args) {
        System.out.print("And Operator : ");
        System.out.println(66>5 && 78>7);
        System.out.print("Or Operator : ");
        System.out.println(66>5 || 7>8);
        System.out.println(2&3);
    }
}
